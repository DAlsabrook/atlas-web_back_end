#!/usr/bin/env python3
"""
2-to_str.py

Module that contains a type-annotated function to_str that takes a float n as
argument and returns the string representation of the float
"""


def to_str(n: float) -> str:
    """Converts float into a string

    Args:
        n (float): float to convert

    Returns:
        str: string representation of the float
    """
    return str(n)

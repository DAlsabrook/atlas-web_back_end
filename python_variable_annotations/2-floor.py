#!/usr/bin/env python3
"""
2-floor.py

This module contains a function floor() which takes a float n as argument and
returns the floor of the float.
"""
import math


def floor(n: float) -> int:
    """Function floor takes a float and returns the floor of that float

    Args:
        n (float): A float to floor

    Returns:
        int: The floor of the float given as an arg
    """
    return math.floor(n)

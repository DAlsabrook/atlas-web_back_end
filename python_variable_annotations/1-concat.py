#!/usr/bin/env python3
"""
1-concat.py

Module for a function to concat strings
"""


def concat(str1: str, str2: str) -> str:
    """concat() concats 2 strings

    Args:
        str1 (str): a string
        str2 (str): a string

    Returns:
        str: another string
    """
    return str1 + str2
